<?php
$a = 1;                 // 将表达式“1”的值赋值给$a
echo $a = 1;            // 输出表达式“$a = 1”的值
echo $a + 4;            // 输出表达式“$a + 4”的值
$a = $a + 4;            // 将表达式“$a + 4”的值赋值给$a
$b = $a = 1;            // 将表达式“$a = 1”的值赋值给$b
echo 5, 6;              // 输出表达式“5”和表达式“6”的值
echo PHP_VERSION;       // 输出表达式“PHP_VERSION”的值
var_dump($b);           // 输出表达式“$b”的值
var_dump($a + $b);      // 输出表达式“$a + $b”的值
